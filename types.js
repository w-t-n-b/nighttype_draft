  const a1 = TYPE_AXES[t1];
  const a2 = TYPE_AXES[t2];
  if (!a1 || !a2) return 2;

  let score = 2; // ベーススコア

  // S/M軸: 補完(S×M)=+1, 衝突(S×S・M×M)=-1
  if (a1.sm !== a2.sm) score += 1;
  else score -= 1;

