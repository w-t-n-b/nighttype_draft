function getCompat(t1, t2) {
  const a1 = TYPE_AXES[t1];
  const a2 = TYPE_AXES[t2];
  if (!a1 || !a2) return 2;

  const keys = ["numa", "sm", "ai", "gt"];
  let match = 0;
  keys.forEach(k => { if (a1[k] === a2[k]) match++; });

  let score;
  const opp = 4 - match;
  const smOpp = a1.sm !== a2.sm;
  const numaOpp = a1.numa !== a2.numa;

  if (match >= 3) score = 5;
  else if (match === 2) score = 4;
  else if (smOpp && match >= 2) score = 3;
  else if (numaOpp && match === 3) score = 3;
  else if (opp >= 3) score = 2;
  else score = 3;

  // 特殊ルール補正
  if (a1.gt === "G" && a2.gt === "G") score += 1; // Give×Give
  if (a1.gt === "T" && a2.gt === "T") score -= 1; // Take×Take
  if (a1.sm === "S" && a2.sm === "S") score -= 1; // S×S
  if (a1.sm === "M" && a2.sm === "M") score -= 1; // M×M

  return Math.max(1, Math.min(5, score));
}

